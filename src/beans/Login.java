package beans;

public class Login {

	private  String login;
	private  String senha; 
	private int hie;

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public String getSenha() {
		return senha;
	}

	public void setSenha(String senha) {
		this.senha = senha;
	}
	
	public void setHie(int hie) {
		this.hie = hie;
	}
	
	public int getHie() {
		return hie;
	}
	
	
}
